def score_features(df1, target):
    
    stack = traceback.extract_stack()
    filename, lineno, function_name, code = stack[-2]
    vars_name = re.compile(r'\((.*?)\).*$').search(code).groups()[0]
    final = (re.findall(r"[\w']+", vars_name))[-1]
    
    x = df1.dtypes.index
    count = -1
    q_columns2 = len(x)
    q_col2 = 0
    q_max = 0
    
    for i in x:
        q_col2 = (q_col2 + 1)
        if int((q_col2/q_columns2)*10) > q_max:
            q_max = int((q_col2/q_columns2)*10)
            print(str(q_max*10)+"% ", end = '')
        if i.upper() not in (final.upper()):
            if np.issubdtype(df1[i], np.number) and len(Series.unique(df1[i])) > 2:
                conv = mono_bin(target, df1[i])
                conv["VAR_NAME"] = i
                count = count + 1
            else:
                conv = char_bin(target, df1[i])
                conv["VAR_NAME"] = i            
                count = count + 1
                
            if count == 0:
                iv_df = conv
            else:
                iv_df = iv_df.append(conv,ignore_index=True)
    
    iv = pd.DataFrame({'IV':iv_df.groupby('VAR_NAME').IV.max()})
    iv = iv.reset_index()
    return(iv_df,iv) 

def plot_roc_curves(y_truth, y_preds_to_compares):  
  # Graficamos la curva ROC del arbol de cada iteracion
  def plot_roc_curve(y_truth, y_pred_proba, label):

    # Calculamos los valores de la curva ROC para graficar
    fpr, tpr, _ = metrics.roc_curve(y_truth,  y_pred_proba)

    # Calculamos el Area Bajo la Curva (AUC) y la guardo
    auc = round(metrics.roc_auc_score(y_truth, y_pred_proba),5)

    # Graficamos
    plt.plot(fpr, tpr, label= str(label)) #+" - AUC="+str(auc))
    plt.legend(loc=4, fontsize=12)

  # Inicializamos los labels del grafico
  plt.figure(figsize=(20, 10))
  plt.xlabel('% Negatives', fontsize=14)
  plt.ylabel('% Positives', fontsize=14)

  # Graficamos la recta del azar
  it = [i/100 for i in range(100)]
  plt.plot(it,it,label="RANDOM, AUC=0.5",color="black")

  # For each prediction set, plot the ROC curve
  for l in y_preds_to_compares.keys():
      plot_roc_curve(y_truth, y_preds_to_compares[l], l)

  # Agregamos el titulo y configuro el tama√±o de letra
  plt.title("ROC Curve", fontsize=14)
  plt.tick_params(labelsize=12);
  plt.show()